/**
 * build-manifest.mjs
 * Walks public/sprites/ and writes src/assets/manifest.ts.
 * Re-run any time sprites change: npm run build:manifest
 *
 * Expected sprite tree:
 *   public/sprites/{category}/{name}/{animation}/{direction}.png
 * e.g. public/sprites/characters/critic-1/idle/front.png
 *
 * Produces a typed const object:
 *   Sprites.characters['critic-1'].idle.front === '/sprites/characters/critic-1/idle/front.png'
 */

import { readdirSync, statSync, existsSync, mkdirSync, writeFileSync } from 'fs';
import { join, relative, sep } from 'path';
import { fileURLToPath } from 'url';

const __dirname = fileURLToPath(new URL('.', import.meta.url));
const ROOT = join(__dirname, '..');
const SPRITES_DIR = join(ROOT, 'public', 'sprites');
const OUT_FILE = join(ROOT, 'src', 'assets', 'manifest.ts');

/** Recursively collect all .png files under a directory. */
function collectPngs(dir) {
  if (!existsSync(dir)) return [];
  const entries = readdirSync(dir, { withFileTypes: true });
  const results = [];
  for (const entry of entries) {
    const full = join(dir, entry.name);
    if (entry.isDirectory()) {
      results.push(...collectPngs(full));
    } else if (entry.isFile() && entry.name.endsWith('.png')) {
      results.push(full);
    }
  }
  return results;
}

/** Build a nested object from an array of path segments, setting the leaf to a URL string. */
function setNested(obj, keys, value) {
  let cur = obj;
  for (let i = 0; i < keys.length - 1; i++) {
    if (!(keys[i] in cur)) cur[keys[i]] = {};
    cur = cur[keys[i]];
  }
  cur[keys[keys.length - 1]] = value;
}

/** Serialize a nested JS object to a TypeScript const literal (no JSON.stringify — no quotes on keys). */
function serialize(obj, indent = 0) {
  const pad = '  '.repeat(indent);
  const innerPad = '  '.repeat(indent + 1);
  if (typeof obj === 'string') return `'${obj}'`;
  const entries = Object.entries(obj);
  if (entries.length === 0) return '{}';
  const lines = entries.map(([k, v]) => {
    const key = /^[a-zA-Z_$][a-zA-Z0-9_$]*$/.test(k) ? k : `'${k}'`;
    return `${innerPad}${key}: ${serialize(v, indent + 1)},`;
  });
  return `{\n${lines.join('\n')}\n${pad}}`;
}

const CATEGORIES = ['characters', 'ghosts', 'stands', 'props'];
const tree = {};

for (const category of CATEGORIES) {
  const catDir = join(SPRITES_DIR, category);
  const pngs = collectPngs(catDir);
  const catObj = {};

  for (const absPath of pngs) {
    // Path relative to public/sprites/category => name/animation/direction.png
    const rel = relative(catDir, absPath);
    const parts = rel.split(sep).map((p) => p.replace(/\.png$/, ''));
    // URL served from public/
    const url = '/sprites/' + category + '/' + rel.split(sep).join('/');
    setNested(catObj, parts, url);
  }

  tree[category] = catObj;
}

// Ensure src/assets/ exists
const outDir = join(ROOT, 'src', 'assets');
if (!existsSync(outDir)) mkdirSync(outDir, { recursive: true });

const banner = `// AUTO-GENERATED by scripts/build-manifest.mjs — do not edit by hand.
// Re-run: npm run build:manifest (any time sprites change)
`;

const body = `export const Sprites = ${serialize(tree)} as const;\n`;

writeFileSync(OUT_FILE, banner + body, 'utf-8');
console.log(`manifest written → ${relative(ROOT, OUT_FILE)}`);
